export function GetHeaderBgByThemeColor(themeColor) {
  switch (themeColor) {
    case 'green':
      return 'bg-green-700';
    case 'blue':
      return 'bg-blue-500';
    case 'cyan':
      return 'bg-cyan-700';
    case 'amber':
      return 'bg-amber-600';
    case 'yellow':
      return 'bg-yellow-500';
    case 'gray':
      return 'bg-gray-700';
    case 'purple':
      return 'bg-purple-700';
    case 'pink':
      return 'bg-pink-700';
    case 'teal':
      return 'bg-teal-700';
    case 'rose':
      return 'bg-rose-500';
    default:
      return 'bg-gray-700';
  }
}

